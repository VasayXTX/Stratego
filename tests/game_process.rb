M = h_slice(Generator::MAP_MINI, %w[name width height])
M['obst'] = Generator::MAP_MINI['structure']['obst']
MAP_SIZE = M['width'] * M['height']


class GameProcess
  def initialize(pl_placement, opp_placement)
    @pl_placement = pl_placement
    @opp_placement = opp_placement
    @turn = :pl
  end

  def make_move(pos_from, pos_to, duel = {})
    @turn, pl_plac, opp_plac, sender, p_from, p_to =
      if @turn == :pl
        [
          :opp,
          @pl_placement,
          @opp_placement,
          0,
          pos_from.to_s,
          pos_to.to_s
        ]
      else
        [
          :pl,
          @opp_placement,
          @pl_placement,
          1,
          (MAP_SIZE - pos_from - 1).to_s,
          (MAP_SIZE - pos_to - 1).to_s
        ]
      end

    request, responses = 
      if duel.empty?
        pl_plac[p_to] = pl_plac[p_from]
        GameProcess.make_move_cmd(pos_from, pos_to, sender)
      else
        results = 
          if duel[:result] == :win
            pl_plac[p_to] = pl_plac[p_from]
            opp_plac.delete(p_to)
            [:win, :loss]
          elsif duel[:result] == :loss
            [:loss, :win]
          else
            opp_plac.delete(p_to)
            [:draw, :draw]
          end
        req_duels = Array.new(2) do |i|
          {
            'attacker'  => duel[:attacker],
            'protector' => duel[:protector],
            'result'    => results[i].to_s
          }
        end
        GameProcess.make_move_duel_cmd(pos_from, pos_to, sender, req_duels)
      end

    pl_plac.delete(p_from)

    _req = { 'cmd' => 'getGame' }
    _resp = get_game

    [
      [sender, request, responses],
      [0, _req, _resp[0]],
      [1, _req, _resp[1]],
    ]
  end

  def self.req_make_move_cmd(p_from, p_to)
    {
      'cmd' => 'makeMove',
      'posFrom' => p_from,
      'posTo' => p_to
    }
  end

  def self.make_move_duel_cmd(p_from, p_to, sender, results)
    res = GameProcess.make_move_cmd(p_from, p_to, sender)

    res[1][sender].merge!('duel' => results[sender])

    opp = (sender + 1) % 2
    res[1][opp].merge!('duel' => results[opp])

    res
  end

  def self.make_move_cmd(p_from, p_to, sender)
    [
      GameProcess.req_make_move_cmd(p_from, p_to),
      {
        sender => { 'status' => 'ok' },
        ((sender + 1) % 2) => {
          'cmd' => 'opponentMakeMove',
          'posFrom' => MAP_SIZE - p_from - 1,
          'posTo' => MAP_SIZE - p_to - 1
        }
      }
    ]
  end

  def get_game
    res = Array.new(2) do |i|
      {
        i => {
          'status' => 'ok',
          'game_name' => GAME_MINI,
          'players' => %w[User0 User1],
          'army' => Generator::ARMY_MINI,
        }
      }
    end
    
    res[0][0].merge!(
      'map' => M,
      'isTurn' => @turn == :pl,
      'state' => {
        'pl1' => clone(@pl_placement),
        'pl2' => clone(@opp_placement.keys.map { |el| el.to_i })
      }
    )
    
    m = clone(M)
    m['obst'] = reflect_a(m['obst'], MAP_SIZE)
    res[1][1].merge!(
      'map' => m,
      'isTurn' => @turn == :opp,
      'state' => {
        'pl1' => reflect_placement(@opp_placement, MAP_SIZE),
        'pl2' => reflect_a(
          @pl_placement.keys.map { |el| el.to_i },
          MAP_SIZE
        )
      }
    )

    res
  end
end

